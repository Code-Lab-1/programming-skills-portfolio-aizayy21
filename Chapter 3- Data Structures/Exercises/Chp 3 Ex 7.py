locations = ['London', 'Paris', 'Dubai', '', 'Germany']

print("It's in a order:")
print(locations)

print("\nAlphabetical:")
print(sorted(locations))

print("\nIt's in a order:")
print(locations)

print("\nReverse alphabetically:")
print(sorted(locations, reverse=True))

print("\nIt's in a order:")
print(locations)

print("\nReversed:")
locations.reverse()
print(locations)

print("\nIt's in a order:")
locations.reverse()
print(locations)

print("\nAlphabetically")
locations.sort()
print(locations)

